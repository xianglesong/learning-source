package org.jboss.netty.util;

/**
 * A common interface for a class which depends on external resources that need explicit release or
 * shutdown.
 *
 * @author The Netty Project (netty-dev@lists.jboss.org)
 * @author Trustin Lee (tlee@redhat.com)
 * @version $Rev: 1587 $, $Date: 2009-07-20 13:50:03 +0900 (Mon, 20 Jul 2009) $
 *
 * @apiviz.landmark
 */
public interface ExternalResourceReleasable {

  /**
   * Releases the external resources that this object depends on. You should not call this method if
   * the external resources (e.g. thread pool) are in use by other objects.
   */
  void releaseExternalResources();
}
